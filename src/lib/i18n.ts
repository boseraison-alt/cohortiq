/**
 * Translate a key to the given language.
 * Supports simple {name} interpolation.
 */
export function t(key: string, lang: Lang = "en", vars?: Record<string, string>): string {
  let str = T[lang]?.[key] ?? T.en[key] ?? key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      str = str.replace(`{${k}}`, v);
    }
  }
  return str;
}

/** Get the display label + flag for a language code */
export function langMeta(code: Lang) {
  return LANGUAGES.find((l) => l.code === code) ?? LANGUAGES[0];
}
